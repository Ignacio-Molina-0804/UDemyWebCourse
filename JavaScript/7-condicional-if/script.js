/// Condicional IF
// Un condicional IF es algo que como dice el nombre es una condicion, este se podria tomar como decir si tomo una coca, tenemos el true que seria en el caso de que la tomemos o el false en el caso de que no

tomeUnaCoca = true

if(tomeUnaCoca){

    // Si se cumple la condicion
    console.log("Hoy me tome una coca");

} else {

    // Si NO se cumple la condicion
    console.log("Hoy no me tome una coca");

}